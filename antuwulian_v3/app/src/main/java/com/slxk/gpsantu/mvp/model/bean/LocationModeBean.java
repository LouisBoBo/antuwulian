package com.slxk.gpsantu.mvp.model.bean;

/**
 * 定位模式bean
 */
public class LocationModeBean {

    private int modeId;
    private String modeName; // 模式名称
    private String description; // 定位模式说明
    private String param; // 需要的附加信息
    private int uid;// UI类型
    private boolean isSelect = false; // 是否选中
    private String modeValue; // 模式设置值
    private String modeNamePlus = ""; //模式名称 + 时间等情况

    public LocationModeBean(int modeId, String name, String description, String param, int uid) {
        super();
        this.modeId = modeId;
        this.modeName = name;
        this.description = description;
        this.param = param;
        this.uid = uid;
    }

    public String getParam() {
        return param;
    }

    public void setParam(String param) {
        this.param = param;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getModeId() {
        return modeId;
    }

    public void setModeId(int modeId) {
        this.modeId = modeId;
    }

    public String getModeName() {
        return modeName;
    }

    public void setModeName(String modeName) {
        this.modeName = modeName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isSelect() {
        return isSelect;
    }

    public void setSelect(boolean select) {
        isSelect = select;
    }


    public String getModeValue() {
        return modeValue;
    }

    public void setModeValue(String modeValue) {
        this.modeValue = modeValue;
    }


    public String getModeNamePlus() {
        return modeNamePlus;
    }

    public void setModeNamePlus(String modeNamePlus) {
        this.modeNamePlus = modeNamePlus;
    }
}
