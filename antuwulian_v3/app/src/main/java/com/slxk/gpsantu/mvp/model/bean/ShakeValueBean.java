package com.slxk.gpsantu.mvp.model.bean;

/**
 * 震动报警-震动灵敏度值
 */
public class ShakeValueBean {

    private int sensitivity; // 灵敏度值，高：18，中：28，低：48
    private String sensitivityName;
    private boolean isSelect; // 是否选中

    public ShakeValueBean(int sensitivity, String name, boolean select){
        super();
        this.sensitivity = sensitivity;
        this.sensitivityName = name;
        this.isSelect = select;
    }

    public int getSensitivity() {
        return sensitivity;
    }

    public void setSensitivity(int sensitivity) {
        this.sensitivity = sensitivity;
    }

    public String getSensitivityName() {
        return sensitivityName;
    }

    public void setSensitivityName(String sensitivityName) {
        this.sensitivityName = sensitivityName;
    }

    public boolean isSelect() {
        return isSelect;
    }

    public void setSelect(boolean select) {
        isSelect = select;
    }
}
